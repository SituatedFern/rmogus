//imports
use std::{thread, time};

use terminal_size::{Width, Height, terminal_size};

fn main() {
    loop { //looping the entire code.
        let size = terminal_size(); //getting the terminal size
        if let Some((Width(w), Height(h))) = size {
            let calc = w as i32 - 30; //accounting for the number of lines that the ascii image takes up.
            print!("\x1B[2J\x1B[1;1H"); //clearing the screen
            let amogus = "⠀";
            for temp in 1..calc {
                let new = amogus.repeat(temp.try_into().unwrap()).to_string();
                suschar(new);

                //creating a delay and then clearing the screen.
                let millis = time::Duration::from_millis(50);
                thread::sleep(millis);
                print!("\x1B[2J\x1B[1;1H"); //clears the screen and moves cursor to first line
            }
            for temp2 in (1..calc).rev() {
                let new = amogus.repeat(temp2.try_into().unwrap()).to_string();
                suschar(new);

                //creating a delay and then clearing the screen.
                let millis = time::Duration::from_millis(50);
                thread::sleep(millis);
                print!("\x1B[2J\x1B[1;1H"); //clears the screen and moves cursor to first line
            }
        } else {
            println!("Unable to get terminal size");
        }
    }
}

